import streamlit as st
import matplotlib.pyplot as plt
import seaborn as sns
import pickle

def display_sentiment_plot():
    # Load the Matplotlib figure object from the .sav file
    with open('sentiment_plot.sav', 'rb') as f:
        loaded_fig = pickle.load(f)

    # Streamlit app
    st.title('Sentiment Analysis Plot')

    # Display the loaded plot using Streamlit
    st.pyplot(loaded_fig)

if __name__ == '__main__':
    display_sentiment_plot()